    public void generarPDF(String n, String u, int ca, float p, float pt) throws FileNotFoundException, DocumentException{
        Document documento = new Document();
        FileOutputStream ficheroPDF = new FileOutputStream("ticket.PDF");
        
        PdfWriter.getInstance(documento, ficheroPDF);
        documento.open();
        
        Paragraph titulo = new Paragraph("Ticket de compra \n\n",
                FontFactory.getFont("arial",
                        22,Font.BOLD, BaseColor.BLUE)
        );
        documento.add(titulo);
        PdfPTable tabla = new PdfPTable(5);
        tabla.addCell("Nombre");
        tabla.addCell("Cliente");
        tabla.addCell("Cantidad");
        tabla.addCell("Costo Unitario");
        tabla.addCell("Costo Total");
        tabla.addCell(n);
        tabla.addCell(u);
        tabla.addCell(Integer.toString(ca));
        tabla.addCell(Float.toString(p));
        tabla.addCell(Float.toString(pt));
        
        documento.add(tabla);
        
        documento.close();
    }
