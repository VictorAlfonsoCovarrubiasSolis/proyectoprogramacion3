    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        if(jRadioButton1.isSelected()){//si lo va a comprar
            try{
                Statement s, s2;
                ResultSet rs;
                
                s = con.createStatement();//se crea un statement sql
                String tablaapartados= ("SELECT * FROM apartados");
                rs= s.executeQuery(tablaapartados);
                while(rs.next()){
                    if(rs.getString("nomproducto").equals(jComboBox1.getSelectedItem().toString())){
                        cantidad=rs.getString("cantidad");
                        precio=rs.getString("costotot");
                        preciouni=rs.getString("costouni");
                    }
                }

                int cantidadtot = Integer.parseInt(cantidad);
                float precio2 = Float.parseFloat(precio);
                float preciouni2 = Float.parseFloat(preciouni);
                s2 = con.createStatement();//se crea un statement sql
                String datosregistro= "INSERT INTO compras (nomproducto, user, cantidad, costouni, costotot)"
                +" VALUES('"+jComboBox1.getSelectedItem().toString()+"','"+usuario+"','"+cantidadtot+"','"+preciouni2+"','"+precio2+"')";//el estado predeterminado del producto sera disponible
                s2.executeUpdate(datosregistro);//se guardan los datos en la tabla productos (la id no se guarda porque se pone sola con la autoincrementacion)
                JOptionPane.showMessageDialog(null, "Comprado", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                //se elimina de apartados
                generarPDF(jComboBox1.getSelectedItem().toString(), usuario, cantidadtot, preciouni2, precio2);
                Statement s3 = con.createStatement();//se crea un statement sql
                String eliminacion= ("Delete from apartados WHERE nombre='"+jComboBox1.getSelectedItem().toString()+"'");
                s3.executeUpdate(eliminacion);//se eliminan de la tabla
                jComboBox1.removeAllItems();//se limpia
                rellenarcombo();//se actualiza
            }catch(SQLException e) {
                System.out.println("Error"+e);
            } catch (FileNotFoundException e) {
                System.out.println("Error"+e);
            } catch (DocumentException e) {
                System.out.println("Error"+e);
            }
            
        }
        if(jRadioButton2.isSelected()){//si se va a eliminar
            try{
                Statement s3 = con.createStatement();//se crea un statement sql
                String eliminacion= ("Delete from apartados WHERE nombre='"+jComboBox1.getSelectedItem().toString()+"'");
                s3.executeUpdate(eliminacion);//se eliminan de la tabla
                JOptionPane.showMessageDialog(null, "Eliminado", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                jComboBox1.removeAllItems();//se limpia
                rellenarcombo();//se actualiza
            }catch(SQLException e) {
                System.out.println("Error"+e);
            }
        }
    }//GEN-LAST:event_ConfirmarActionPerformed

     public void actualizartabla(){
        Statement s;
        DefaultTableModel modelo = new DefaultTableModel();
        //Encabezados de columnas
        modelo.addColumn("nomproducto");
        modelo.addColumn("user");
        modelo.addColumn("cantidad");
        modelo.addColumn("costouni");
        modelo.addColumn("costotot");

        jTable1.setModel(modelo);
        String datos[] = new String[5];
        
        try{
            
            s = con.createStatement();//se crea un statement sql
            String tablausers= ("Select * from apartados WHERE user='"+usuario+"'");
            ResultSet rs;   
            rs= s.executeQuery(tablausers);
            
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);

                modelo.addRow(datos);
            }
        }catch(SQLException e){
            System.out.println("Error: "+e);
        }
    }
