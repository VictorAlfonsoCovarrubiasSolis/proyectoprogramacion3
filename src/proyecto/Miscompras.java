    public void actualizartabla(){
        Statement s;
        DefaultTableModel modelo = new DefaultTableModel();
        //Encabezados de columnas
        modelo.addColumn("nomproducto");
        modelo.addColumn("user");
        modelo.addColumn("cantidad");
        modelo.addColumn("costouni");
        modelo.addColumn("costotot");

        jTable1.setModel(modelo);
        String datos[] = new String[5];
        
        try{
            
            s = con.createStatement();//se crea un statement sql
            String tablausers= ("Select * from compras WHERE user='"+usuario+"'");
            ResultSet rs;   
            rs= s.executeQuery(tablausers);
            
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);

                modelo.addRow(datos);
            }
        }catch(SQLException e){
            System.out.println("Error: "+e);
        }
    }
